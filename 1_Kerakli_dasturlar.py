# SPYDER IDE
# =============================================================================

# Spyder — Python tilida kod yozich uchun mo'ljallangan dasturlash muhiti va 
# bizning asosiy ish qurolimizdir. Ingliz tilida dasturlash muhiti - 
# IDE (Integrated Development Environment) deyiladi.

# Dastur yozish uchun yuzlab turli muhitlar bor. Aslida, umuman muhitsiz, oddiy 
# matn redaktorida ham kod yozishimiz mumkin edi. Lekin Spyder va unga o'xshash 
# muhitlar dasturchilarning ishini yengillatishga qaratilgan. Muhitlar turli 
# qo'shimcha funktsiyalar va foydali xossalarga boy bo'ladi. 

# Kelajakda siz o'zingiz uchun qulay muhitni tanlab, yangi dasturlarni yangi muhitda
# yozishingiz mumkin. Boshlanishiga esa biz Anaconda bilan birga keladigan qulay va 
# sodda, lekin shu bilan birga funktsiyalarga boy bo'lgan Spyder muhitini tanlaymiz. 

# Spyder dasturini Anaconda Navigator yoki Windows orqali to'g'ridan-to'g'ri 
# ochish mumkin.

